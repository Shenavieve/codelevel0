function celciusToFahrenheit (num) {
  var fahrenheit = (num * 9/5) + 32; 
  console.log (fahrenheit + " degrees fahrenheit");
}
celciusToFahrenheit (32);
 
 function fahrenheitToCelcius(num1){
   var celcius = (num1 - 32) * 5/9;
   console.log(celcius + " degrees celcius");
 }
 fahrenheitToCelcius(32)
